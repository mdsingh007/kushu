@app.route("/")
def hello_world():
    data = db.sql_fetch()
    rows = []
    for elem in data:
        row = f"<tr><td>{elem[0]}</td><td>{elem[1]}</td><td>{elem[2]}</td><td>{elem[3]}</td><td>{elem[4]}</td></tr>"
        rows.append(row)

    rows = "".join(rows)

    html = f'''
    <table border='4px'>
        <tr>
            <th>Id</th>
            <th>Name</th>
            <th>Phone</th>
            <th>Home Phone</th>
            <th>Address</th>
        </tr>
        {rows}
    </table>
    '''

    return html




